    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblManMenuIP.getModel();
        dtm.setRowCount(0);
        for(Menu menu : menudirectory.getMenuDirectory()){
            if(menu.getRestaurantName().equals(useraccount.getEmployee().getName())) {
                Object [] row = new Object[2];
                row[0] = menu;
                row[1] = menu.getPrice();
                dtm.addRow(row);
            }
        }
    }

