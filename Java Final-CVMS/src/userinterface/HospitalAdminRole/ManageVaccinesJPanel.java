    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
        
        if(txtIN.getText().isEmpty() || txtPrice.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,"Fields cannot be empty");
            return;
        }
        
        String itemName = txtIN.getText();
        double price;
        try {
            price = Double.parseDouble(txtPrice.getText());
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Doses required should be a numeric value.");
            return;
        }
        
        String res = useraccount.getEmployee().getName();
        
        menudirectory.newItem(itemName, price, res);
        populateTable();
        
        clearAll();
    }//GEN-LAST:event_btnAddItemActionPerformed

